#include "libft.h"

int	ft_printf(const char *format, ...)
{
	va_list	args;
	int		i;
	int		count;

	i = 0;
	count = 0;
	va_start(args, format);
	while (format[i])
	{
		if (format[i] == '%')
		{
			i++;
			count = ft_formator(args, format[i], count);
		}
		else
			count += ft_putchar(format[i]);
		i++;
	}
	va_end (args);
	return (count);
}
