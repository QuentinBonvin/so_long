#include "libft.h"

int	ft_len_number(unsigned long int n)
{
	int		len;

	len = 0;
	if (n == 0)
	{
		len = 1;
	}
	while (n > 0)
	{
		n = n / 16;
		len++;
	}
	return (len);
}
